package AS;

public class ChargeAS {
	private String asDate;
	private int asNum;
	private int conNum;
	private String asDetail;
	private String asBudget;
	private int asEx;
	public String getAsDate() {
		return asDate;
	}
	public void setAsDate(String asDate) {
		this.asDate = asDate;
	}
	public int getAsNum() {
		return asNum;
	}
	public void setAsNum(int asNum) {
		this.asNum = asNum;
	}
	public int getConNum() {
		return conNum;
	}
	public void setConNum(int conNum) {
		this.conNum = conNum;
	}
	public String getAsDetail() {
		return asDetail;
	}
	public void setAsDetail(String asDetail) {
		this.asDetail = asDetail;
	}
	public String getAsBudget() {
		return asBudget;
	}
	public void setAsBudget(String asBudget) {
		this.asBudget = asBudget;
	}
	public int getAsEx() {
		return asEx;
	}
	public void setAsEx(int asEx) {
		this.asEx = asEx;
	}
	

}
