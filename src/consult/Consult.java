package consult;

public class Consult {
	int conNum; 
	int cliNum;
	String conSize;
	String budget;
	String conAddr;
	String conDate;
	String contents;
	int inteEx;
	public int getConNum() {
		return conNum;
	}
	public void setConNum(int conNum) {
		this.conNum = conNum;
	}
	public int getCliNum() {
		return cliNum;
	}
	public void setCliNum(int cliNum) {
		this.cliNum = cliNum;
	}
	public String getConSize() {
		return conSize;
	}
	public void setConSize(String conSize) {
		this.conSize = conSize;
	}
	public String getBudget() {
		return budget;
	}
	public void setBudget(String budget) {
		this.budget = budget;
	}
	public String getConAddr() {
		return conAddr;
	}
	public void setConAddr(String conAddr) {
		this.conAddr = conAddr;
	}
	public String getConDate() {
		return conDate;
	}
	public void setConDate(String conDate) {
		this.conDate = conDate;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public int getInteEx() {
		return inteEx;
	}
	public void setInteEx(int inteEx) {
		this.inteEx = inteEx;
	}

}
